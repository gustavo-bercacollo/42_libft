#include "ft_string.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	size_t	src_len;

	src_len = ft_strlen(s);
	dest = malloc((src_len + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
